function cRP.checkPermissions(homeName)
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		local identity = vRP.getUserIdentity(user_id)
		if identity then
			if not vRP.wantedReturn(user_id) then
				local homeResult = vRP.query("vRP/get_homepermissions",{ home = tostring(homeName) })
				if parseInt(#homeResult) >= 1 then
					local myResult = vRP.query("vRP/get_homeuser",{ user_id = parseInt(user_id), home = tostring(homeName) })
					if myResult[1] then
						return true
					else
						if not unlocked[homeName] then
							TriggerClientEvent("Notify",source,"vermelho","A porta está trancada.",3000)
							return false
						else
							return true
						end
					end
				else
					local getInvoice = vRP.getInvoice(user_id)
					if getInvoice[1] ~= nil then
						TriggerClientEvent("Notify",source,"vermelho","Encontramos faturas pendentes.",3000)
						return
					end

					local getFines = vRP.getFines(user_id)
					if getFines[1] ~= nil then
						TriggerClientEvent("Notify",source,"vermelho","Encontramos multas pendentes.",3000)
						return
					end

					local ok = vRP.request(source,"Comprar a residência <b>"..tostring(homeName).."</b> por <b>$"..vRP.format(parseInt(homes[tostring(homeName)][2])).." dolares</b>?",30)
					if ok then
						if vRP.paymentBank(user_id,parseInt(homes[tostring(homeName)][2])) then
							vRP.execute("vRP/buy_permissions",{ home = tostring(homeName), user_id = parseInt(user_id), vault = parseInt(homes[tostring(homeName)][4]) })
						else
							TriggerClientEvent("Notify",source,"vermelho","Dinheiro insuficiente.",3000)
						end
					end
					return false
				end
			end
		end
	end
	return false
end
